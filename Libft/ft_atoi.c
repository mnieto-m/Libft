#include "libft.h"

int	ft_atoi(const char *str)
{
	int	dest;
	int	sign;

	dest = 0;
	sign = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = sign * (-1);
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		dest = (dest * 10) + *str - 48;
		str++;
	}
	return (dest * sign);
}
