#include "libft.h"

int check_token(char *s, char c)
{
	int i;
	int count;
	i = -1;
	count = 0;
	while (s[++i])
	{
		if (s[i] == '\n')
			continue;
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			count++;
		// i++;
	}
	return (count);
}
