#include "libft.h"

int	check_token(char *s, char c)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (s[i] == c) 
    {
        i++;
    }

	while (s[i])
	{
		if (s[i] != c)
		{
			if((s[i + 1] == c || s[i + 1] == '\0')&& s[i] != '\n')
				count++;
		}
		i++;
	}
	return (count);
}
