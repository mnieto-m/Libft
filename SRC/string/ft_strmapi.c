#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*aux;

	i = 0;
	aux = (char *)malloc(ft_strlen(s) + 1);
	if (!aux)
		return (0);
	while (s[i] != '\0')
	{
		aux[i] = f(i, s[i]);
		i++;
	}
	aux[i] = '\0';
	return (aux);
}
